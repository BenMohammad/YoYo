package com.benmohammad.yoyo.snippets;

public class Snip {

    private String title;
    private String content;

    public Snip(String title, String content) {
        this.title = title;
        this.content = content;
    }

    public String getTitle() {
        return this.title;
    }

    public String getContent() {
        return this.content;

    }}
